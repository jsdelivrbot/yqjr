package com.yqjr.fin.mkmm.sales.condition;

import org.hibernate.validator.constraints.Length;

import java.util.Date;

/**
 * 活动制定
 * Created by yaoxin on 2018/6/6.
 */
public class ActiveMainListCondition {
    /**
     * 所属公司
     */
    @Length(min = 0, max = 100)
    private java.lang.String company;
    /**
     * 活动编码
     */
    @Length(min = 0, max = 20)
    private java.lang.String activeCode;

    /**
     * 活动名称
     */
    @Length(min = 0, max = 64)
    private java.lang.String activeName;

    /**
     * 区域代码
     */
    @Length(min = 0, max = 100)
    private java.lang.String areaCode;

    /**
     * 区域名称
     */
    @Length(min = 0, max = 1000)
    private java.lang.String areaName;

    /**
     * 受理开始时间
     */
    @Length(min = 0, max = 50)
    private java.lang.String activeStartTime;

    /**
     * 受理结束时间
     */
    @Length(min = 0, max = 50)
    private java.lang.String activeEndTime;

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getAreaCode() {
        return areaCode;
    }

    public void setAreaCode(String areaCode) {
        this.areaCode = areaCode;
    }

    public String getActiveCode() {
        return activeCode;
    }

    public void setActiveCode(String activeCode) {
        this.activeCode = activeCode;
    }

    public String getActiveName() {
        return activeName;
    }

    public void setActiveName(String activeName) {
        this.activeName = activeName;
    }

    public String getAreaName() {
        return areaName;
    }

    public void setAreaName(String areaName) {
        this.areaName = areaName;
    }

    public String getActiveStartTime() {
        return activeStartTime;
    }

    public void setActiveStartTime(String activeStartTime) {
        this.activeStartTime = activeStartTime;
    }

    public String getActiveEndTime() {
        return activeEndTime;
    }

    public void setActiveEndTime(String activeEndTime) {
        this.activeEndTime = activeEndTime;
    }
}
