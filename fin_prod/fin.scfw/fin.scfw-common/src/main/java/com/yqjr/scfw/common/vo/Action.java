package com.yqjr.scfw.common.vo;

import javax.validation.constraints.NotNull;
import java.util.Map;

/**
 * @author quangz
 */
public class Action {
    /**
     * action name
     */
    @NotNull
    private String name;

    /**
     * params
     */
    private Map params;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Map getParams() {
        return params;
    }

    public void setParams(Map params) {
        this.params = params;
    }
}
